# 9. Compute Euclidean distance between any two NumPy vectors.

import numpy as np
vec1 = np.array([1, 1])
vec2 = np.array([5, 4])
euclidean_distance = np.sqrt(np.sum((vec1 - vec2)**2))
print("Vector 1:", vec1)
print("Vector 2:", vec2)
print("Euclidean Distance :", euclidean_distance)
